# --- Fonction couleurs selon temps ---
def get_color(time):
    if time <= 5:
        return 'dodgerblue'
    elif time <= 10:
        return 'mediumblue'
    elif time <= 15:
        return 'darkorange'
    else:
        return 'firebrick'

# Style unique: dashArray '5,5' pour tous
def get_style(color):
    return {'color': color, 'weight': 5, 'dashArray': '5'}

def make_style_func(style_dict):
    def style_function(feature):
        return style_dict
    return style_function


# Style pour diff√©rence des temps
def get_color_d_vel_voi(diff):
    if diff > 5:
        return 'mediumvioletred'
    elif 0 < diff <= 5:
        return 'hotpink'
    elif diff == 0:
        return 'gold'
    else:
        return 'limegreen'

