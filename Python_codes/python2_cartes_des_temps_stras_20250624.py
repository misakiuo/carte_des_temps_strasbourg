def get_color(time):
    if time <= 5:
        return 'dodgerblue'
    elif time <= 10:
        return 'mediumblue'
    elif time <= 15:
        return 'darkorange'
    else:
        return 'firebrick'

def get_style(source, color):
    if source == 'tram':
        return {'color': color, 'weight': 4, 'dashArray': '0'}
    else:
        return {'color': color, 'weight': 4, 'dashArray': '5, 5'}

